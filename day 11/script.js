scrollToTopBtn.style.boxShadow = '0 4px 8px rgba(0, 0, 0, 0.2)';

document.body.appendChild(scrollToTopBtn);

scrollToTopBtn.addEventListener('click', () => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
});

